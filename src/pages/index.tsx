import { type NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import bg from "../../public/images/home/bg.png";
import kite from "../../public/images/home/kite.jpg";
import { Button, Container } from "../components/index";
import Navbar from "~/components/Navbar";
import { motion, useScroll, useTransform } from "framer-motion";
import { useRef } from "react";

const Home: NextPage = () => {
  const ref = useRef<HTMLDivElement>(null);
  const { scrollYProgress } = useScroll({
    target: ref,
    offset: ["end end", "end start"],
  });

  const scale = useTransform(scrollYProgress, [0, 1], [1, 0.8]);
  const opacity = useTransform(scrollYProgress, [0, 1], [1, 0]);

  const imageScale = useTransform(scrollYProgress, [0, 1], [1, 1.2]);
  const gradientOpacity = useTransform(scrollYProgress, [0, 1], [0.6, 0]);

  return (
    <>
      <Head>
        <title>Airborne</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className=" relative  h-screen w-screen">
        <Navbar />

        <div ref={ref} className="fixed">
          <motion.div
            style={{ opacity: gradientOpacity, scale: imageScale }}
            className="fixed top-0 z-10  h-screen  w-screen bg-gradient-to-br from-black to-transparent opacity-60"
          ></motion.div>

          <motion.div style={{ scale: imageScale, opacity }}>
            <Image
              src={bg}
              alt="kiteboarder jumping"
              className="z-0 h-screen  w-screen object-cover"
            ></Image>
          </motion.div>
          <Container maxWidth="8xl" className="z-0 m-auto">
            <motion.div
              style={{ scale, opacity }}
              className="absolute bottom-0 z-20 flex flex-col gap-6 pb-16"
            >
              <h1 className="max max-w-xl text-7xl font-bold">
                Take Your Kitesurfing to New Heights
              </h1>
              <p className="max text-md z-0 max-w-lg font-light">
                Airborne Kiteboarding offers a wide range of kitesurfing gear
                for all levels of riders, from beginner packages to
                high-performance gear.
              </p>
              <Button color="primary" padding="wide" className=" self-start">
                SHOP NOW
              </Button>
            </motion.div>
          </Container>
        </div>
      </main>

      <div className=" w-full ">
        <KitesSection />
      </div>
    </>
  );
};

export default Home;

